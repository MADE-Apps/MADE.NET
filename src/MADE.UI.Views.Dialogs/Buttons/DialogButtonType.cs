namespace MADE.UI.Views.Dialogs.Buttons
{
    /// <summary>
    /// Defines values associated with the type of a dialog button.
    /// </summary>
    public enum DialogButtonType
    {
        /// <summary>
        /// The button is neutral and can be used to perform any action.
        /// </summary>
        Neutral,

        /// <summary>
        /// The button is confirming and can be used to perform positive actions.
        /// </summary>
        Confirm,

        /// <summary>
        /// The button is cancelling and can be used to perform negative actions.
        /// </summary>
        Cancel
    }
}